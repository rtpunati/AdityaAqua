<strong style="color:black;">2.What does the shrimp "count per pound" mean? How is the size of shrimp calculated?
</strong><br><b>A)</b>Understanding shrimp sizing can be tricky. Chances are, you’ve noticed a number on the front of a bag of shrimp. Is it referring to the size of the shrimp or the number of shrimp in the bag? In most cases, it’s referring to the actual size. Shrimp size is designated by “count per pound,” meaning, if the number reads “26/30 ct/lb,” that’s the number of shrimp per each pound. It does not necessarily mean there are 26-30 shrimp in the package. For example, a 2-pound bag of size 26/30 ct/lb shrimp would have 52-60 shrimp in the bag. The lower the number of shrimp per pound, the larger the shrimp is, and vice versa.

